func TestGetSlotById(t *testing.T) {
	if err := godotenv.Load(".env.test"); err != nil {
		log.Fatalf("Error loading .env.test file: %v", err)
	}

	doctor, err := graphql.CreateDoctor(model.CreateDoctorInput{
		Email:     "test_doctor_get_slot@edgar-sante.fr",
		Password:  "password",
		Name:      "name",
		Firstname: "first",
		Address: &model.AddressInput{
			Street:  "",
			ZipCode: "",
			Country: "",
			City:    "",
		},
		Status: true,
	})
	if err != nil {
		t.Errorf("Error while creating patient: %v", err)
	}

	input := CreateSlotInput{
		StartDate: 10,
		EndDate:   12,
	}

	createSlot := CreateSlot(input, doctor.ID)
	if createSlot.Code != 201 {
		t.Errorf("Expected code 201 but got %d", createSlot.Code)
	}
	if createSlot.Err != nil {
		t.Errorf("Expected no error but got: %s", createSlot.Err.Error())
	}

	response := GetSlotById(createSlot.Rdv.ID, doctor.ID)
	if response.Code != 200 {
		t.Errorf("Expected code 200 but got %d", response.Code)
	}
	if response.Err != nil {
		t.Errorf("Expected no error but got: %s", response.Err.Error())
	}
}

